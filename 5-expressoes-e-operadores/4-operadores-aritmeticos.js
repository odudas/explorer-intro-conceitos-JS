// Operadores Aritméticos

// multiplicação *
console.log(3 * 5);

// divisão /
console.log(12 / 2);

// soma +
console.log(34 + 67);

// subtração -
console.log(34 - 23);

// resto da divisão %
console.log(10 % 3);

// incremento ++
let incremento = 0;
incremento++;
console.log(incremento);

// decremento --
let decremento = 1;
decremento--;
console.log(decremento);

// exponencial **
console.log(3 ** 3);
